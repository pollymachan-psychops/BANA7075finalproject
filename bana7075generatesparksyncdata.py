def generate_sparksync_clinical_dataset(n=500):
    # --- 1. Expanded Clinical Constants ---
    # Core Diagnoses
    mh_dx = ['MDD', 'GAD', 'PTSD', 'Bipolar I', 'Bipolar II', 'OCD', 'Schizoaffective']

    # Substance Use Disorders (SUD)
    sud_dx = ['SUD-Alcohol', 'SUD-Cannabis', 'SUD-Opiate', 'SUD-Amphetamine', 'Polysubstance Use']

    # Personality Disorders (Target ~10% of patients)
    personality_dx = ['BPD', 'Avoidant PD', 'Dependent PD', 'Narcissistic PD', 'ASPD']

    # Expanded Modalities
    modalities = [
        'ERP (Exposure Response Prevention)', 'RO-DBT', 'Standard DBT',
        'Mentalization-Based Treatment (MBT)', 'Psychodynamic Therapy',
        'Transitional Life Skills', 'CBT', 'EMDR', 'Addictions Recovery (12-Step)',
        'Addictions Recovery (SMART)', 'Somatic Experiencing'
    ]

    data = []
    for i in range(n):
        # --- 2. Diagnosis Logic (The 10% Rule) ---
        # Pick 1-4 diagnoses total
        p_val = random.random()
        current_patient_dx = []

        if p_val < 0.10: # 10% get a Personality Disorder
            current_patient_dx.append(random.choice(personality_dx))

        # Add 1-2 Mental Health or SUD diagnoses
        current_patient_dx.extend(random.sample(mh_dx + sud_dx, random.randint(1, 2)))

        # Ensure at least 5 diagnosis slots are filled (for the 10 slots requirement)
        dx_padded = current_patient_dx + [None] * (10 - len(current_patient_dx))

        # --- 3. Weighted Modality Logic ---
        # If OCD is present, prioritize ERP/RO-DBT
        # If PD is present, prioritize MBT/Psychodynamic
        patient_priorities = []
        if 'OCD' in current_patient_dx:
            patient_priorities = ['ERP (Exposure Response Prevention)', 'RO-DBT']
        elif any(pd in current_patient_dx for pd in personality_dx):
            patient_priorities = ['Mentalization-Based Treatment (MBT)', 'Psychodynamic Therapy']

        # Fill remaining slots from the general list
        remaining_needed = 5 - len(patient_priorities)
        available_modalities = [m for m in modalities if m not in patient_priorities]
        patient_priorities.extend(random.sample(available_modalities, remaining_needed))

        # --- 4. Patient Record Construction ---
        patient = {
            'mrn': f"MRN-{random.randint(100000, 999999)}",
            'age': np.random.randint(18, 85),
            'gender': random.choice(['Male', 'Female', 'Non-binary']),
            'primary_diagnosis': current_patient_dx[0],
            'secondary_diagnosis': current_patient_dx[1] if len(current_patient_dx) > 1 else 'None',

            # Clinical Needs Weights
            'priority_1': patient_priorities[0],
            'priority_2': patient_priorities[1],
            'priority_3': patient_priorities[2],
            'priority_4': patient_priorities[3],
            'priority_5': patient_priorities[4],

            # Life Stage & Motivation
            'life_stage': 'Emerging Adult' if 18 <= i <= 25 else 'Adult',
            'motivation': random.choice(['Internally driven', 'Mixed', 'Family pressures']),
            'transition_focus': 'Strong Treatment' if random.random() > 0.3 else 'Life Skills/Transitional',

            # Coordinator Feedback
            'coordinator_comfort': round(np.random.uniform(3.5, 5.0), 1),
            'last_elected_program': f"Facility_{random.randint(1, 50)}"
        }

        # Add the 10 diagnosis columns
        for idx, dx in enumerate(dx_padded):
            patient[f'diagnosis_{idx+1}'] = dx

        data.append(patient)

    df = pd.DataFrame(data)
    df.to_csv('sparksync_clinical_data_v2.csv', index=False)
    return df

# Run the generation
