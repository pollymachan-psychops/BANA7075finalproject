def generate_sparksync_complete_v2(n=500):
    # 1. Setup Constants
    mh_dx = ['MDD', 'GAD', 'PTSD', 'Bipolar I', 'Bipolar II', 'OCD', 'Schizoaffective']
    sud_dx = ['SUD-Alcohol', 'SUD-Cannabis', 'SUD-Opiate', 'SUD-Amphetamine', 'Polysubstance Use']
    personality_dx = ['BPD', 'Avoidant PD', 'Dependent PD', 'Narcissistic PD', 'ASPD']
    modalities = ['ERP', 'RO-DBT', 'Standard DBT', 'MBT', 'Psychodynamic', 'Life Skills', 'CBT', 'EMDR']

    data = []
    for i in range(n):
        # Personality Disorder Logic (10%)
        current_dx = [random.choice(personality_dx)] if random.random() < 0.10 else [random.choice(mh_dx)]
        current_dx.extend(random.sample(mh_dx + sud_dx, 2))

        # Modality Logic
        p = ['ERP', 'RO-DBT'] if 'OCD' in current_dx else ['MBT', 'Psychodynamic'] if any(x in current_dx for x in personality_dx) else random.sample(modalities, 2)
        p.extend(random.sample([m for m in modalities if m not in p], 3))

        patient = {
            'mrn': f"MRN-{random.randint(100000, 999999)}",
            'name': f"Patient_{i}",
            'age': np.random.randint(18, 85),
            'primary_diagnosis': current_dx[0],
            'priority_1': p[0],
            'priority_2': p[1],
            'transition_focus': 'Strong Treatment' if random.random() > 0.3 else 'Life Skills',
            'coordinator_comfort': round(np.random.uniform(3.5, 5.0), 1)
        }
        data.append(patient)

    # CREATE THE DATAFRAME
    df = pd.DataFrame(data)

    # 2. INJECT DIRTY DATA (Now 'df' is defined in this scope!)
    df.loc[0, 'age'] = 155
    df.loc[1, 'mrn'] = np.nan
    df.loc[2, 'primary_diagnosis'] = None
    df.loc[3, 'transition_focus'] = ""
    df.loc[4, 'mrn'] = "ERROR_999_###"

    # 3. SAVE
    df.to_csv('sparksync_disposition_dirty_v2.csv', index=False)
    print("✅ Success! 'sparksync_disposition_dirty_v2.csv' created with clinical logic and 5 errors.")
    return df

